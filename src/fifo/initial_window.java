    class Proc{
        String name;
        int tiempoLlegada;
        int tiempoEjecucuion;
        int tiempoEspera;
        int tiempoRetorno;
        
        
        public Proc(String name, int tiempoLlegada, int tiempoEjecucion){
            this.name = name;
            this.tiempoEjecucuion = tiempoEjecucion;
            this.tiempoLlegada = tiempoLlegada;
            
        }
    }
    private void ejec_proc2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ejec_proc2MouseClicked
        var e = new Proc("proc1", llegada, 5);        
        e.tiempoEspera = esperaTodos - e.tiempoLlegada;
        llegada++;
        
         secundary_window ven = new secundary_window(e.name);
        ven.setVisible(true);
        
        retornoTodos += e.tiempoEjecucuion;
        e.tiempoRetorno = retornoTodos;
        
        procesos.add(e);
        
        for (Proc proceso : procesos) {
            System.out.print(proceso.name + " ");
        }
        System.out.println("");
        
        
    }//GEN-LAST:event_ejec_proc2MouseClicked

